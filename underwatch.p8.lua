function make_player()
	temp_entity = {}

	temp_entity.pos = {}
		temp_entity.pos.x = rnd(64)
		temp_entity.pos.y = rnd(128)

	temp_entity.sca = {}
		temp_entity.sca.x = 8
		temp_entity.sca.y = 8

	temp_entity.velocity = {}
		temp_entity.velocity.x = rnd(2)
		temp_entity.velocity.y = rnd(2)

