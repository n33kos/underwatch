-------------------entities
function make_player()
	player_entities = {}
	player_entities[1] = {}

	player_entities[1].pos = {}
		player_entities[1].pos.x = rnd(128)
		player_entities[1].pos.y = rnd(128)

	player_entities[1].sca = {}
		player_entities[1].sca.x = 8
		player_entities[1].sca.y = 8

	player_entities[1].velocity = {}
		player_entities[1].velocity.x = rnd(2)
		player_entities[1].velocity.y = rnd(2)

	player_entities[1].mass = 1
	player_entities[1].jumpheight = 10
	player_entities[1].isjumping = true

	player_entities[1].sprite = 0
	player_entities[1].current_animation = "walk"

	player_entities[1].animations = {}
		player_entities[1].animations.idle = {0}
		player_entities[1].animations.walk = {1,1,1,1,2,2,2,2}
		player_entities[1].animations.jump = {1}

	player_entities[1].spriteflip = {}
		player_entities[1].spriteflip.x = true
		player_entities[1].spriteflip.y = false

	player_entities[1].shottimer = 0
	player_entities[1].projectile = {}
		player_entities[1].projectile.parent = "player"
		player_entities[1].projectile.sprite = 16
		player_entities[1].projectile.mass = 0.2
		player_entities[1].projectile.maxage = 5
		player_entities[1].projectile.firedelay = 10 --draw frames between shots
		player_entities[1].projectile.velocity = {}
			player_entities[1].projectile.velocity.x = 10
			player_entities[1].projectile.velocity.y = 0
		player_entities[1].projectile.sca = {}
			player_entities[1].projectile.sca.x = 2
			player_entities[1].projectile.sca.y = 2

end

function make_ai()
	for i=0,6 do
		ai_entities[i] = {}
		
		ai_entities[i].pos = {}
		ai_entities[i].pos.x = rnd(128)
		ai_entities[i].pos.y = rnd(128) - phys.ground_height
		
		ai_entities[i].sca = {}
		ai_entities[i].sca.x = 8
		ai_entities[i].sca.y = 8

		ai_entities[i].velocity = {}
		ai_entities[i].velocity.x = rnd(2)
		ai_entities[i].velocity.y = rnd(2)

		ai_entities[i].mass = 1
		ai_entities[i].jumpheight = 10
		ai_entities[i].isjumping = true

		ai_entities[i].sprite = 32
		ai_entities[i].current_animation = "idle"

		ai_entities[i].animations = {}
			ai_entities[i].animations.idle = {32}
			ai_entities[i].animations.walk = {32,32,32}
			ai_entities[i].animations.jump = {32}

		ai_entities[i].spriteflip = {}
		ai_entities[i].spriteflip.x = true
		ai_entities[i].spriteflip.y = false

	end
end

function make_projectile(sprite, mass, sca, pos, velocity, maxage, direction, parent)
	proj = {}
	proj.age = 0
	proj.maxage = maxage
	proj.parent = parent
	proj.pos = {}
		proj.pos.x = pos.x
		proj.pos.y = pos.y
	proj.sca = {}
		proj.sca.x = sca.x
		proj.sca.y = sca.y
	proj.velocity = {}
		if direction == true then
			proj.velocity.x = velocity.x*-1
		else
			proj.velocity.x = velocity.x
		end
		proj.velocity.y = velocity.y
	proj.mass = mass
	proj.sprite = sprite
	proj.spriteflip = {}
		proj.spriteflip.x = direction
		proj.spriteflip.y = false
	proj.id = #projectiles

	add(projectiles, proj)
end

------------physics
function apply_gravity(entity)
	if entity.isjumping then
		entity.velocity.y = entity.velocity.y-phys.gravity[2]*entity.mass
	end
end

function apply_velocity(entity)
	entity.pos.x = entity.pos.x+entity.velocity.x
	entity.pos.y = entity.pos.y+entity.velocity.y
end

function apply_drag(entity)
	entity.velocity.x /= phys.drag[1]
	entity.velocity.y /= phys.drag[2]
end

function apply_solid_body_collision(entity)
	val = mget(flr((entity.pos.x+(entity.sca.x/2))/8), flr((entity.pos.y+entity.sca.y)/8))
	if fget(val, 7) then
		entity.velocity.y = 0
		entity.isjumping = false
	end

	val = mget(flr((entity.pos.x+(entity.sca.x*1.5))/8), flr((entity.pos.y+(entity.sca.y/2))/8))
	if fget(val, 7) then
		entity.velocity.x = 0
	end

	val = mget(flr((entity.pos.x-(entity.sca.x*1.5))/8), flr((entity.pos.y+(entity.sca.y/2))/8))
	if fget(val, 7) then
		entity.velocity.x = 0
	end
end

function apply_projectile_collision(entity, table, parenttype)
	for key,bullet in pairs(projectiles) do
		c = bullet.pos.x;
		d = bullet.pos.y;
		a = c+bullet.sca.x;
		b = d+bullet.sca.y;

		y = entity.pos.x;
		z = entity.pos.y;
		w = y+entity.sca.x;
		x = z+entity.sca.y;

		intersect = true
		if(a<w and c<w and a<y and c<y) intersect = false
		if(a>w and c>w and a>y and c>y) intersect = false
		if(b<x and d<x and b<z and d<z) intersect = false
		if(b>x and d>x and b>z and d>z) intersect = false
		if intersect and bullet.parent != parenttype then
			del(table, entity)
			del(projectiles, bullet)
		end
	end
end

-------------drawing
function draw_entity(entity)
	spr(entity.sprite, entity.pos.x, entity.pos.y, entity.sca.x/8, entity.sca.y/8, entity.spriteflip.x, entity.spriteflip.y)
end

function draw_map()
	map(0, 0, 0, 0, 64, 32)
end

function move_camera()
	camera(cam.pos.x,cam.pos.y)
	if player_entities[1] then
		cam.pos.x = cam.pos.x + (player_entities[1].pos.x - (cam.pos.x+cam.offset.x))/cam.followdistance.x
		cam.pos.y = cam.pos.y + (player_entities[1].pos.y - (cam.pos.y+cam.offset.y))/cam.followdistance.y
	end
end

function set_animation_frame(entity, animation)
	if animation == "idle" then
		entity.sprite = entity.animations.idle[phys.time % #entity.animations.idle+1]
	end
	if animation == "walk" then
		entity.sprite = entity.animations.walk[phys.time % #entity.animations.walk+1]
	end
	if animation == "jump" then
		entity.sprite = entity.animations.jump[phys.time % #entity.animations.jump+1]
	end
end

-----------pico functions--------------
---------------------------------------
function _init()
	cls()
	make_player()
	make_ai()
end

function _update()
	--ai entities
	for key,entity in pairs(ai_entities) do
		apply_gravity(entity)
		apply_velocity(entity)
		apply_solid_body_collision(entity)
		apply_drag(entity)
		apply_projectile_collision(entity, ai_entities, "ai")
	end

	--player entities
	for key,entity in pairs(player_entities) do
		apply_gravity(entity)
		apply_velocity(entity)
		apply_solid_body_collision(entity)
		apply_drag(entity)
		apply_projectile_collision(entity, player_entities, "player")
		set_animation_frame(entity, entity.current_animation)
	end

	--projectiles
	for key,entity in pairs(projectiles) do
		apply_gravity(entity)
		apply_velocity(entity)
		entity.age += 1
		if entity.age >= entity.maxage then
			del(projectiles, entity)
		end
	end

end

function _draw()
	cls()
	phys.time += 1

	-----------------------player input----------------------
	---------------------------------------------------------
	for key,entity in pairs(player_entities) do
		entity.current_animation = "idle"
	end
	if btn(0) then
		--left
		for key,entity in pairs(player_entities) do
			entity.velocity.x -= 0.25
			entity.spriteflip.x = true
			entity.current_animation = "walk"
		end
	end
	if btn(1) then
		--right
		for key,entity in pairs(player_entities) do
			entity.velocity.x += 0.25
			entity.spriteflip.x = false
			entity.current_animation = "walk"
		end
	end
	if btn(2) then
		--up
		for key,entity in pairs(player_entities) do
			if entity.isjumping == false then
				entity.velocity.y -= entity.jumpheight
				entity.isjumping = true
			end
		end
	end
	if btn(3) then
		--down
	end
	if btn(4) then
		--one
		for key,entity in pairs(player_entities) do
			entity.shottimer -= 1
			if entity.shottimer <= 0 then
				entity.shottimer = entity.projectile.firedelay

				--adjust bullet start location
				adjustedpos = {}
				if entity.spriteflip.x then
					adjustedpos.x = entity.pos.x
				else
					adjustedpos.x = entity.pos.x+8
				end
				adjustedpos.y = entity.pos.y+3

				make_projectile(entity.projectile.sprite, entity.projectile.mass, entity.projectile.sca, adjustedpos, entity.projectile.velocity, entity.projectile.maxage, entity.spriteflip.x, "player")
			end
		end
	end
	if btn(5) then
		--two
	end

	--camera
	move_camera()
	--map
	draw_map()
	--ai entities
	for key,entity in pairs(ai_entities) do
		draw_entity(entity)
	end
	--player entities
	for key,entity in pairs(player_entities) do
		if entity.isjumping then
			entity.current_animation = "jump"
		end
		draw_entity(entity)
	end
	--projectiles
	for key,entity in pairs(projectiles) do
		draw_entity(entity)
	end
end


