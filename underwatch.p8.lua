		temp_entity.projectile.maxage = 5
		temp_entity.projectile.bounce = false
		temp_entity.projectile.firedelay = 10 --draw frames between shots
		temp_entity.projectile.velocity = {}
			temp_entity.projectile.velocity.x = 10
			temp_entity.projectile.velocity.y = 0
		temp_entity.projectile.sca = {}
			temp_entity.projectile.sca.x = 2
			temp_entity.projectile.sca.y = 1
		temp_entity.projectile.pixeloffset = {}
			temp_entity.projectile.pixeloffset.x = 0
			temp_entity.projectile.pixeloffset.y = 3

