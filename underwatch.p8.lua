function make_ai()
	for i=1,2 do
		temp_entity = {}
		
		temp_entity.pos = {}
		temp_entity.pos.x = rnd(64)+64
		temp_entity.pos.y = rnd(128) - phys.ground_height
		
		temp_entity.sca = {}
		temp_entity.sca.x = 8
		temp_entity.sca.y = 8

		temp_entity.velocity = {}
		temp_entity.velocity.x = rnd(2)
		temp_entity.velocity.y = rnd(2)

		temp_entity.mass = 1
		temp_entity.speed = 0.1
		temp_entity.jumpheight = 5
		temp_entity.isjumping = true
		temp_entity.ismortal = true

		temp_entity.sprite = 32
		temp_entity.current_animation = "idle"

		temp_entity.animations = {}
			temp_entity.animations.idle = {32}
